/// [MadColorPicker]
class MadColorPicker extends StatelessWidget {
  /// [color] use for initial color basically [Colors.white]
  final Color selectedColor;

  /// [onColorSelected] function happens after picking a color
  final Function(Color?)? onColorSelected;

  final IconData iconData;

  const MadColorPicker({
    Key? key,
    this.selectedColor = Colors.white,
    this.iconData = Icons.colorize,
    this.onColorSelected,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(0.0),
      width: MediaQuery.of(context).size.width,
      child: Row(
        children: [
          Padding(
            padding: const EdgeInsets.symmetric(horizontal: 8.0),
            child: SelectedColor(
              iconData: iconData,
              selectedColor: selectedColor,
            ),
          ),
          Expanded(
            child: SizedBox(
              height: 52,
              child: PageView(
                controller: PageController(
                  initialPage: 0,
                  keepPage: true,
                ),
                physics: const BouncingScrollPhysics(),
                children: [
                  Row(
                    children: createColors(context, ColorsConstant.colors1),
                  ),
                  Row(
                    children: createColors(context, ColorsConstant.colors2),
                  ),
                  Row(
                    children: createColors(context, ColorsConstant.colors3),
                  ),
                ],
              ),
            ),
          ),
        ],
      ),
    );
  }

  /// create colors list item with safe width size.
  List<Widget> createColors(BuildContext context, List<Color> colors) {
    double size = _correctSizes[colors.length] ??
        _correctButtonSize(colors.length, MediaQuery.of(context).size.width);
    if (colors.length > 9) {}
    return [
      for (var color in colors)
        GestureDetector(
          child: Padding(
            padding: EdgeInsets.all(size * 0.1),
            child: AnimatedContainer(
              width: size,
              height: size,
              duration: const Duration(milliseconds: 200),
              decoration: BoxDecoration(
                color: color,
                shape: BoxShape.circle,
                border: Border.all(
                    width: color == selectedColor ? 4 : 2, color: Colors.white),
                boxShadow: [
                  BoxShadow(blurRadius: size * 0.1, color: Colors.black)
                ],
              ),
            ),
          ),
          onTap: () => onColorSelected!.call(color),
        )
    ];
  }
}

double _correctButtonSize(int itemSize, double screenWidth) {
  bool isSizeSafe = false;
  double initialSize = 52;
  double maxWidth = screenWidth - initialSize;
  double finalSize = 48;

  do {
    finalSize -= 2;
    double eachSize = finalSize * 1.2;
    double buttonArea = itemSize * eachSize;
    isSizeSafe = maxWidth > buttonArea;
  } while (!isSizeSafe);
  _correctSizes[itemSize] = finalSize;
  return finalSize;
}

/// [SelectedColor] widget
class SelectedColor extends StatelessWidget {
  final Color selectedColor;
  final IconData? iconData;

  const SelectedColor({Key? key, required this.selectedColor, this.iconData})
      : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Container(
      width: 36,
      height: 36,
      decoration: BoxDecoration(
        color: selectedColor,
        shape: BoxShape.circle,
        border: Border.all(width: 2, color: Colors.white),
        boxShadow: const [BoxShadow(blurRadius: 0, color: Colors.black38)],
      ),
      child: iconData != null
          ? Icon(
              iconData,
              color: (selectedColor.computeLuminance() > 0.5)
                  ? Colors.black
                  : Colors.white,
            )
          : null,
    );
  }
}

class MadIconPicker extends StatelessWidget {
  final IconData selectedIconData;
  final Function(IconData)? onIconDataSelected;

  

  const MadIconPicker({
    Key? key,
    this.onIconDataSelected,
    required this.selectedIconData,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(0.0),
      width: MediaQuery.of(context).size.width,
      child: Row(
        children: [
          
          Padding(
            padding: const EdgeInsets.symmetric(horizontal: 8.0),
            child: SelectedIconData(
              iconData: selectedIconData,
              selectedIconData: selectedIconData,
            ),
          ),
          Expanded(
              child: SizedBox(
            height: 52,
            child: PageView(
              controller: PageController(initialPage: 0),
              physics: const BouncingScrollPhysics(),
              children: [
                Row(
                  children: createIconData(context, IconDatasConstant.icon1),
                )
              ],
            ),
          ))
        ],
      ),
    );
  }

  List<Widget> createIconData(BuildContext context, List<IconData> iconDatas) {
    double size = _correctSizes[iconDatas.length] ??
        _correctButtonSize(iconDatas.length, MediaQuery.of(context).size.width);

    if (iconDatas.length > 9) {
      throw ErrorWidget.builder = (errorDetail) {
        return const Text('IconData list needs to be <= 9 in one set');
      };
    }
    return [
      for (var iconData in iconDatas)
        GestureDetector(
          child: Padding(
            padding: EdgeInsets.all(size * 0.1),
            child: AnimatedContainer(
              width: size,
              height: size,
              duration: const Duration(milliseconds: 200),
              decoration: BoxDecoration(
                color: Colors.white,
                shape: BoxShape.circle,
                boxShadow: [
                  BoxShadow(blurRadius: size * 0.1, color: Colors.black)
                ],
              ),
              child: Icon(
                iconData,
                size: 24,
              ),
            ),
          ),
          onTap: () {
            onIconDataSelected!.call(iconData);
          },
        )
    ];
  }
}

class SelectedIconData extends StatelessWidget {
  final IconData selectedIconData;
  final IconData? iconData;

  const SelectedIconData({
    Key? key,
    required this.selectedIconData,
    this.iconData,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Container(
      width: 36,
      height: 36,
      decoration: BoxDecoration(
        color: Colors.white,
        shape: BoxShape.circle,
        border: Border.all(width: 2, color: Colors.white),
        boxShadow: const [BoxShadow(blurRadius: 0, color: Colors.black)],
      ),
      child: iconData != null
          ? Icon(
              iconData,
            )
          : null,
    );
  }
}



  
